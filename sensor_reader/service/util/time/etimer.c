#include "etimer.h"


void etimer_init(etimer_t *timer, float expire)
{
   timer->expire = expire;
   etimer_reset(timer);
}


void etimer_reset(etimer_t *timer)
{
   timer->state = 0.0f;
}


int etimer_check(etimer_t *timer, float dt)
{
   timer->state += dt;
   if (timer->state > timer->expire)
   {
      timer->state = timer->expire;
   }
   return timer->state == timer->expire;
}

